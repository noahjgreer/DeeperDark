package net.minecraft.client.render.debug;

import java.util.Iterator;
import net.fabricmc.api.EnvType;
import net.fabricmc.api.Environment;
import net.minecraft.client.MinecraftClient;
import net.minecraft.client.render.VertexConsumerProvider;
import net.minecraft.client.util.math.MatrixStack;
import net.minecraft.fluid.FluidState;
import net.minecraft.registry.tag.FluidTags;
import net.minecraft.util.math.BlockPos;
import net.minecraft.util.math.Box;
import net.minecraft.world.WorldView;

@Environment(EnvType.CLIENT)
public class WaterDebugRenderer implements DebugRenderer.Renderer {
   private final MinecraftClient client;

   public WaterDebugRenderer(MinecraftClient client) {
      this.client = client;
   }

   public void render(MatrixStack matrices, VertexConsumerProvider vertexConsumers, double cameraX, double cameraY, double cameraZ) {
      BlockPos blockPos = this.client.player.getBlockPos();
      WorldView worldView = this.client.player.getWorld();
      Iterator var11 = BlockPos.iterate(blockPos.add(-10, -10, -10), blockPos.add(10, 10, 10)).iterator();

      BlockPos blockPos2;
      FluidState fluidState;
      while(var11.hasNext()) {
         blockPos2 = (BlockPos)var11.next();
         fluidState = worldView.getFluidState(blockPos2);
         if (fluidState.isIn(FluidTags.WATER)) {
            double d = (double)((float)blockPos2.getY() + fluidState.getHeight(worldView, blockPos2));
            DebugRenderer.drawBox(matrices, vertexConsumers, (new Box((double)((float)blockPos2.getX() + 0.01F), (double)((float)blockPos2.getY() + 0.01F), (double)((float)blockPos2.getZ() + 0.01F), (double)((float)blockPos2.getX() + 0.99F), d, (double)((float)blockPos2.getZ() + 0.99F))).offset(-cameraX, -cameraY, -cameraZ), 0.0F, 1.0F, 0.0F, 0.15F);
         }
      }

      var11 = BlockPos.iterate(blockPos.add(-10, -10, -10), blockPos.add(10, 10, 10)).iterator();

      while(var11.hasNext()) {
         blockPos2 = (BlockPos)var11.next();
         fluidState = worldView.getFluidState(blockPos2);
         if (fluidState.isIn(FluidTags.WATER)) {
            DebugRenderer.drawString(matrices, vertexConsumers, String.valueOf(fluidState.getLevel()), (double)blockPos2.getX() + 0.5, (double)((float)blockPos2.getY() + fluidState.getHeight(worldView, blockPos2)), (double)blockPos2.getZ() + 0.5, -16777216);
         }
      }

   }
}
