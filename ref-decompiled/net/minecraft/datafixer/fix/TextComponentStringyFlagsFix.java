package net.minecraft.datafixer.fix;

import com.mojang.datafixers.DataFix;
import com.mojang.datafixers.TypeRewriteRule;
import com.mojang.datafixers.schemas.Schema;
import com.mojang.datafixers.types.Type;
import com.mojang.serialization.Dynamic;
import java.util.Optional;
import net.minecraft.datafixer.TypeReferences;

public class TextComponentStringyFlagsFix extends DataFix {
   public TextComponentStringyFlagsFix(Schema outputSchema) {
      super(outputSchema, false);
   }

   protected TypeRewriteRule makeRule() {
      Type type = this.getInputSchema().getType(TypeReferences.TEXT_COMPONENT);
      return this.fixTypeEverywhere("TextComponentStringyFlagsFix", type, (dynamicOps) -> {
         return (pair) -> {
            return pair.mapSecond((either) -> {
               return either.mapRight((pair) -> {
                  return pair.mapSecond((pairx) -> {
                     return pairx.mapSecond((pair) -> {
                        return pair.mapSecond((dynamic) -> {
                           return dynamic.update("bold", TextComponentStringyFlagsFix::method_66136).update("italic", TextComponentStringyFlagsFix::method_66136).update("underlined", TextComponentStringyFlagsFix::method_66136).update("strikethrough", TextComponentStringyFlagsFix::method_66136).update("obfuscated", TextComponentStringyFlagsFix::method_66136);
                        });
                     });
                  });
               });
            });
         };
      });
   }

   private static Dynamic method_66136(Dynamic dynamic) {
      Optional optional = dynamic.asString().result();
      return optional.isPresent() ? dynamic.createBoolean(Boolean.parseBoolean((String)optional.get())) : dynamic;
   }
}
